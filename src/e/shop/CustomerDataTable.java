    private void btnLoadDataTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadDataTableActionPerformed
        DefaultTableModel tableMode = (DefaultTableModel) RegisterCustomerDataTable.getModel();
        tableMode.setRowCount(0);

        try {

            Connection myConnect = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "1234");
            Statement myStatement = myConnect.createStatement();
            ResultSet myRS = myStatement.executeQuery("select * from customerinfo ");
            while (myRS.next()) {
                String username = myRS.getString("username");
                String mobilenumber = myRS.getString("mobilenumber");
                String email = myRS.getString("email");
                String address = myRS.getString("address");
                String password = myRS.getString("password");
                String confirmpassword = myRS.getString("confirmpassword");

                tableMode.addRow(new Object[]{username, mobilenumber, email, address, password, confirmpassword});

            }
            myStatement.close();
        } catch (SQLException ex) {
            Logger.getLogger(CustomerDataTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoadDataTableActionPerformed

